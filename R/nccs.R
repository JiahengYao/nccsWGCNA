nccs<- function(humangene,mousegene,humandata,mousedata,humannet,mousenet,humanmodcolor,mousemodcolor,k=20,linecoloralpha=c(0.40,0.40,0.40,0.40),mosttimes=500,quitname='result of nccsWGCNA',means=c("Human Transformational Adjacency Network","Mouse Transformational Adjacency Network","Human-Mouse Transformational Adjacency Network","Mouse-Human Transformational Adjacency Network"),nettype='net') {
