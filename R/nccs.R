  if (nettype=='TOM'){humannames2=unlist(attr(humannet,'dimnames')[1])}


  mouseTOM.matrix = as.matrix(mouseconsTomDS)
  if (nettype=='net'){mousenames2=unlist(attr(mousenet$colors ,'names'))}
  if (nettype=='TOM'){mousenames2=unlist(attr(mousenet,'dimnames')[1])}

