    mousereducedTOM3=((mousereducedTOM)*km)^i/max(((mousereducedTOM)*km)^i)*k
    if(mean(UpperExpansion(mousereducedTOM3))<1){
      #print(mean(UpperExpansion(mousereducedTOM3)))
      #print(i)
      mousehj5=i
      break
    }}


