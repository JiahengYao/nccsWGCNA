    }
  }
  humannames2=generesult
  generesult=unlist(list())
  genelist=mousenames2
  species='mouse'
  if(length(union(genelist,unlist(geneidlist[3])))>length(union(genelist,unlist(geneidlist[2])))){#和人合并，显然并集小的为其现有归属
    flag=2
  }else{flag=3}
  #print(flag)
