    humanreducedTOM3=((humanreducedTOM)*kh)^i/max(((humanreducedTOM)*kh)^i)*k
    if(mean(UpperExpansion(humanreducedTOM3))<1){
      #print(mean(UpperExpansion(humanreducedTOM3)))
      #print(i)
      humanhj6=i
      break
    }}

  for (i in seq(from=humanhj6-0.000011,to=humanhj6+0.000011,by=0.000001)){
    humanreducedTOM3=((humanreducedTOM)*kh)^i/max(((humanreducedTOM)*kh)^i)*k
    if(mean(UpperExpansion(humanreducedTOM3))<1){
      #print(mean(UpperExpansion(humanreducedTOM3)))
      #print(i)
      humanhj7=i
      break
    }}


  for (i in seq(from=humanhj7-0.0000011,to=humanhj7+0.0000011,by=0.0000001)){
    humanreducedTOM3=((humanreducedTOM)*kh)^i/max(((humanreducedTOM)*kh)^i)*k
    if(mean(UpperExpansion(humanreducedTOM3))<1){
      #print(mean(UpperExpansion(humanreducedTOM3)))
      #print(i)
      humanhj8=i
      break
    }}


  km=1/mean(as.numeric(unlist(attr(table(mousereducedTOM),'dimnames')))[2:length(as.numeric(unlist(attr(table(mousereducedTOM),'dimnames'))))])

  for (i in seq(from=1,to=mosttimes,by=1)){
    mousereducedTOM3=((mousereducedTOM)*km)^i/max(((mousereducedTOM)*km)^i)*k
    if(mean(UpperExpansion(mousereducedTOM3))<1){
      #print(mean(UpperExpansion(mousereducedTOM3)))
      #print(i)
      mousehj1=i
      break
    }}

  for (i in seq(from=mousehj1-1.1,to=mousehj1+1.1,by=0.1)){
    mousereducedTOM3=((mousereducedTOM)*km)^i/max(((mousereducedTOM)*km)^i)*k
    if(mean(UpperExpansion(mousereducedTOM3))<1){
      #print(mean(UpperExpansion(mousereducedTOM3)))
      #print(i)
      mousehj2=i
      break
    }}

  for (i in seq(from=mousehj2-0.11,to=mousehj2+0.11,by=0.01)){
    mousereducedTOM3=((mousereducedTOM)*km)^i/max(((mousereducedTOM)*km)^i)*k
    if(mean(UpperExpansion(mousereducedTOM3))<1){
      #print(mean(UpperExpansion(mousereducedTOM3)))
      #print(i)
      mousehj3=i
      break
    }}


  for (i in seq(from=mousehj3-0.011,to=mousehj3+0.011,by=0.001)){
    mousereducedTOM3=((mousereducedTOM)*km)^i/max(((mousereducedTOM)*km)^i)*k
